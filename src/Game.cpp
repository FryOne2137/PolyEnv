void Game::newGame(const NewGameConfig& cfg) {
    // 1) aktywne plemiona na mapie
    map.setActiveTribes(cfg.tribes);

    // 2) generacja mapy
    MapGenerator::Params p;
    p.mapSize = cfg.mapSize;
    p.initialLand = cfg.initialLand;
    p.smoothing = cfg.smoothing;
    p.relief = cfg.relief;
    p.seed = cfg.seed;

    MapGenerator::generate(map, p);

    // Capitals generated by MapGenerator (same order as cfg.tribes / player indices)
    const auto& capitals = MapGenerator::getLastCapitals();

    cities.clear();
    cities.resize(cfg.tribes.size());

    for (size_t i = 0; i < cfg.tribes.size(); ++i) {
        City& c = cities[i];

        // Default city setup for all tribes
        c.setCityId(static_cast<CityId>(i));
        c.setOwnerId(static_cast<uint8_t>(i));
        c.setLevel(1);
        c.setPopulation(0);
        c.setStarsPerRound(1);
        c.setName(std::string("City ") + std::to_string(i));

        // Capitals generated by MapGenerator: each starting city is a capital
        c.setCapital(true);

        // Special tribe rule: Luxidoor starts with a strong capital
        if (cfg.tribes[i] == TribeType::Luxidoor) {
            c.setLevel(3);
            c.setStarsPerRound(4);
            c.setPopulation(0); // reasonable population for level 3
        }
    }

    // 3) tworzymy graczy
    players.clear();
    players.reserve(cfg.tribes.size());

    for (size_t i = 0; i < cfg.tribes.size(); ++i) {
        PlayerId pid = static_cast<PlayerId>(i);

        // Startowe gwiazdki bierzemy z definicji plemienia (wiki defaults)
        const Tribe tribe = Tribe::makeDefault(cfg.tribes[i]);
        const int startStars = static_cast<int>(tribe.getStartStars());

        // capitalId is stable and matches MapGenerator (we use player index as capital/city id)
        const int capitalId = static_cast<int>(i);
        players.emplace_back(pid, cfg.tribes[i], startStars, capitalId);
        // Grant starting technology (new TechId-based tech system)
        const TechId startTech = tribe.getStartTech();
        if (startTech != TechId::Count) {
            players.back().addTech(startTech);
        }
    }

    // 4) jednostki startowe (na start np. warrior w stolicy)
    units.clear();
    // NIE nadpisuj kafelków mapy tutaj — MapGenerator już je wygenerował.

    // 4b) spawn starting units on capitals (simple default: Warrior on each capital)
    // 4b) spawn starting units on capitals (tribe default start unit)
    if (capitals.size() == cfg.tribes.size()) {
        for (size_t i = 0; i < players.size(); ++i) {
            const Pos cap = capitals[i];
            if (!map.inBounds(cap) || map.unitOn(cap) != Map::kNoUnit) continue;

            const Tribe tribe = Tribe::makeDefault(cfg.tribes[i]);
            const UnitType startType = tribe.getStartUnit().getType(); // Oumaji => Rider

            const UnitId uid = spawnUnit(startType, static_cast<PlayerId>(i), cap);
            if (uid != kNoUnit) {
                cities[i].addUnit(uid);
            }
        }
    }
    currentPlayer = 0;
    turnNumber = 0;

    // 5) start tury gracza 0
    TurnSystem::startTurn(*this);
}

bool Game::endTurn() {
    // kończ turę aktualnego gracza
    TurnSystem::endTurn(*this);

    // następny gracz
    currentPlayer = static_cast<PlayerId>((currentPlayer + 1) % players.size());
    if (currentPlayer == 0) {
        turnNumber++;
    }

    // rozpocznij turę nowego gracza
    TurnSystem::startTurn(*this);
    return true;
}

Unit* Game::getUnit(UnitId id) {
    if (id == kNoUnit) return nullptr;
    if (id >= units.size()) return nullptr;
    return &units[id];
}

const Unit* Game::getUnit(UnitId id) const {
    if (id == kNoUnit) return nullptr;
    if (id >= units.size()) return nullptr;
    return &units[id];
}

UnitId Game::spawnUnit(UnitType type, PlayerId owner, Pos pos) {
    if (!map.inBounds(pos)) return kNoUnit;
    if (map.unitOn(pos) != Map::kNoUnit) return kNoUnit;

    UnitId id = static_cast<UnitId>(units.size());

    // ✅ CREATE WITH FACTORY
    Unit u = UnitFactory::create(type, owner, pos);
    u.setId(id);

    units.push_back(u);
    map.setUnitOn(pos, id);
    players[owner].addUnit(id);

    // Register in the owner's capital city (current mapping: cityId == playerId).
    if (owner != kNoPlayer && static_cast<size_t>(owner) < cities.size()) {
        cities[static_cast<size_t>(owner)].addUnit(id);
    }

    VisionSystem::revealFromUnit(*this, id);
    return id;
}


bool Game::moveUnit(UnitId unitId, Pos to) {
    Unit* u = getUnit(unitId);
    if (!u) return false;

    // kolejka tur
    if (!isPlayersTurn(u->getOwnerId())) return false;

    bool ok = MovementSystem::move(*this, unitId, to);
    if (!ok) return false;

    // interakcje: ruin/starfish/village itd.
    InteractionSystem::onUnitEnteredTile(*this, unitId, to);
    return true;
}

bool Game::attack(UnitId attackerId, Pos target) {
    Unit* a = getUnit(attackerId);
    if (!a) return false;

    // kolejka tur
    if (!isPlayersTurn(a->getOwnerId())) return false;

    return CombatSystem::attack(*this, attackerId, target);
}

bool Game::foundCityFromVillage(PlayerId owner, Pos pos) {
    if (!map.inBounds(pos)) return false;

    Tile& tile = map.at(pos);

    // Accept both cases:
    // - still a Village (caller didn't flip yet)
    // - already flipped to City by Interaction
    const SettlementTypeEnum st = tile.getSettlementType();
    if (st != SettlementTypeEnum::Village && st != SettlementTypeEnum::City) return false;

    // Resolve / allocate a settlement id.
    SettlementId sid = tile.getSettlementId();
    CityId cid = kNoCity;

    if (sid != kNoSettlement) {
        cid = static_cast<CityId>(sid);
        if (cid < 0) return false;
        if (static_cast<size_t>(cid) >= cities.size()) {
            cities.resize(static_cast<size_t>(cid) + 1);
        }
    } else {
        cid = static_cast<CityId>(cities.size());
        cities.resize(cities.size() + 1);
        sid = static_cast<SettlementId>(cid);
    }

    City& c = cities[static_cast<size_t>(cid)];

    // Initialize / overwrite city data
    c.setCityId(cid);
    c.setOwnerId(static_cast<uint8_t>(owner));
    c.setLevel(1);
    c.setPopulation(0);
    c.setStarsPerRound(1);
    c.setCapital(false);
    if (c.getName().empty()) {
        c.setName(std::string("City ") + std::to_string(static_cast<int>(cid)));
    }

    // Ensure tile points to this city id.
    tile.setSettlement(SettlementTypeEnum::City, sid);

    return true;
}