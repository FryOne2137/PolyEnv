// Remove unit from any city it is currently registered in, then add it to `newCityId`.
// This keeps City::units consistent when a unit captures/founds a city.
static void reassignUnitToCity(std::vector<City>& cities, UnitId uid, CityId newCityId) {
    if (uid == kNoUnit || newCityId == kNoCity) return;

    // Remove from any old city
    for (City& c : cities) {
        c.removeUnit(uid);
    }

    // Add to new city (if it exists)
    const size_t idx = static_cast<size_t>(newCityId);
    if (idx < cities.size()) {
        cities[idx].addUnit(uid);
    }
}

// Claim free (unassigned) territory in radius 1 around a city center.
// "Free" means: the tile is not already assigned to any city territory.
static void claimFreeTerritoryRadius1(Map& map, CityId cid, SettlementId citySid, Pos center) {
    if (cid == kNoCity) return;

    auto tryClaim = [&](Pos p) {
        if (!map.inBounds(p)) return;
        Tile& tt = map.at(p);

        // Do not override existing territory ownership.
        if (tt.getTerritoryCityId() != kNoCity) return;

        // Do not steal another city's center tile.
        if (tt.getSettlementType() == SettlementTypeEnum::City) {
            const SettlementId otherSid = tt.getSettlementId();
            if (otherSid != kNoSettlement && otherSid != citySid) return;
        }

        tt.setTerritoryCityId(cid);
    };

    // City tile itself
    tryClaim(center);

    // 8-neighborhood around the city (distance 1)
    static const std::array<Pos, 8> kNb = {
        Pos{ 1, 0}, Pos{-1, 0}, Pos{0, 1}, Pos{0,-1},
        Pos{ 1, 1}, Pos{ 1,-1}, Pos{-1, 1}, Pos{-1,-1}
    };
    for (const Pos d : kNb) {
        tryClaim(Pos{center.x + d.x, center.y + d.y});
    }
}

