bool Game::attack(PlayerId pid, UnitId attackerId, Pos target) {
    Unit* a = getUnit(attackerId);
    if (!a) return false;

    // Must be the owner's unit.
    if (pid != a->getOwnerId()) return false;

    // Always enforce turn ownership.
    if (!isPlayersTurn(pid)) return false;

    const bool ok = CombatSystem::attack(*this, attackerId, target);
    if (ok) {
        // Mark that this player attacked during this turn (Pacifist task tracking).
        getPlayer(pid).setAttackedThisTurn(true);

        // Re-check kill-based monuments after the attack.
        MonumentSystem::onKillsUpdated(*this, pid, static_cast<int>(getPlayer(pid).getKillerCount()));
    }
    return ok;
}

bool Game::attack(UnitId attackerId, Pos target) {
    Unit* a = getUnit(attackerId);
    if (!a) return false;
    return attack(a->getOwnerId(), attackerId, target);
}

