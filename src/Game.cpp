bool Game::captureCityAt(PlayerId newOwner, Pos pos) {
    if (!map.inBounds(pos)) return false;

    Tile& tile = map.at(pos);
    if (tile.getSettlementType() != SettlementTypeEnum::City) return false;

    City* c = getCityBySettlementId(tile.getSettlementId());
    if (!c) return false;

    const PlayerId oldOwner = static_cast<PlayerId>(c->getOwnerId());
    if (oldOwner == newOwner) return false;

    // transfer owner
    c->setOwnerId(static_cast<uint8_t>(newOwner));

    // update player city lists (zakładam że masz)
    getPlayer(oldOwner).removeCity(c->getCityId());
    getPlayer(newOwner).addCity(c->getCityId());

    // Move the capturing unit's city assignment to the captured city.
    {
        const UnitId uOn = map.unitOn(pos);
        if (uOn != Map::kNoUnit) {
            reassignUnitToCity(cities, uOn, c->getCityId());
        }
    }

    // tile ownership marker / visuals
    // tile.setTribe(getPlayer(newOwner).getTribeType());

    // keep settlement id stable
    tile.setSettlement(SettlementTypeEnum::City, tile.getSettlementId());

    // Ensure territory is at least initialized around the city center.
    claimFreeTerritoryRadius1(map, c->getCityId(), tile.getSettlementId(), pos);

    return true;
}

