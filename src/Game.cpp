void Game::newGame(const NewGameConfig& cfg) {
    // 1) aktywne plemiona na mapie
    map.setActiveTribes(cfg.tribes);

    // 2) generacja mapy
    MapGenerator::Params p;
    p.mapSize = cfg.mapSize;
    p.initialLand = cfg.initialLand;
    p.smoothing = cfg.smoothing;
    p.relief = cfg.relief;
    p.seed = cfg.seed;

    MapGenerator::generate(map, p);

    // Capitals generated by MapGenerator (same order as cfg.tribes / player indices)
    const auto& capitals = MapGenerator::getLastCapitals();

    cities.clear();
    cities.resize(cfg.tribes.size());

    for (size_t i = 0; i < cfg.tribes.size(); ++i) {
        City& c = cities[i];

        // Default city setup for all tribes
        c.setCityId(static_cast<CityId>(i));
        c.setOwnerId(static_cast<uint8_t>(i));
        c.setLevel(1);
        c.setPopulation(0);
        c.setStarsPerRound(20);
        c.setName(std::string("City ") + std::to_string(i));

        // Capitals generated by MapGenerator: each starting city is a capital
        c.setCapital(true);

        // Special tribe rule: Luxidoor starts with a strong capital
        if (cfg.tribes[i] == TribeType::Luxidoor) {
            c.setLevel(3);
            c.setStarsPerRound(4);
            c.setPopulation(0); // reasonable population for level 3
        }
    }

    // 3) tworzymy graczy
    players.clear();
    players.reserve(cfg.tribes.size());

    for (size_t i = 0; i < cfg.tribes.size(); ++i) {
        PlayerId pid = static_cast<PlayerId>(i);

        // Startowe gwiazdki bierzemy z definicji plemienia (wiki defaults)
        const Tribe tribe = Tribe::makeDefault(cfg.tribes[i]);
        const int startStars = static_cast<int>(tribe.getStartStars());

        // capitalId is stable and matches MapGenerator (we use player index as capital/city id)
        const int capitalId = static_cast<int>(i);
        players.emplace_back(pid, cfg.tribes[i], startStars, capitalId);
        // Ensure player's city list contains the starting capital (needed for tech price scaling).
        players.back().addCity(static_cast<CityId>(capitalId));
        // Grant starting technology (new TechId-based tech system)
        const TechId startTech = tribe.getStartTech();
        if (startTech != TechId::Count) {
            players.back().addTech(startTech);
        }
    }

    // 4) jednostki startowe (na start np. warrior w stolicy)
    units.clear();
    // NIE nadpisuj kafelków mapy tutaj — MapGenerator już je wygenerował.

    // 4b) spawn starting units on capitals (simple default: Warrior on each capital)
    // 4b) spawn starting units on capitals (tribe default start unit)
    if (capitals.size() == cfg.tribes.size()) {
        for (size_t i = 0; i < players.size(); ++i) {
            const Pos cap = capitals[i];
            if (!map.inBounds(cap) || map.unitOn(cap) != Map::kNoUnit) continue;

            const Tribe tribe = Tribe::makeDefault(cfg.tribes[i]);
            const UnitType startType = tribe.getStartUnit().getType(); // Oumaji => Rider

            // Ensure the capital tile is linked to the capital City via SettlementId.
            // IMPORTANT: do NOT change tile tribe here (tile tribe = biome/terrain).
            {
                Tile& t = map.at(cap);
                const CityId cid = cities[i].getCityId();
                const SettlementId sid = static_cast<SettlementId>(cid);
                t.setSettlement(SettlementTypeEnum::City, sid);

                // Initialize territory for the starting capital so Map View can show ownership & highlight.
                claimFreeTerritoryRadius1(map, cid, sid, cap);
            }

            const UnitId uid = spawnUnit(startType, static_cast<PlayerId>(i), cap,true);
            if (uid != kNoUnit) {
                cities[i].addUnit(uid);
            }
        }
    }

    // 4c) initial fog-of-war: each player sees radius 2 around their capital.
    if (capitals.size() == players.size()) {
        for (size_t i = 0; i < players.size(); ++i) {
            VisionSystem::revealArea(*this, static_cast<PlayerId>(i), capitals[i], 2);
        }
    }

    // Also reveal from all units once (in case units have different vision rules).
    for (size_t i = 0; i < players.size(); ++i) {
        VisionSystem::revealForPlayerFromUnits(*this, static_cast<PlayerId>(i));
    }
    currentPlayer = 0;
    turnNumber = 0;

    // 5) start tury gracza 0
    TurnSystem::startTurn(*this);
}

bool Game::endTurn() {
    // kończ turę aktualnego gracza
    TurnSystem::endTurn(*this);

    // następny gracz
    currentPlayer = static_cast<PlayerId>((currentPlayer + 1) % players.size());
    if (currentPlayer == 0) {
        turnNumber++;
    }

    // rozpocznij turę nowego gracza
    TurnSystem::startTurn(*this);
    return true;
}

