bool Game::foundCityFromVillage(PlayerId owner, Pos pos) {
    if (!map.inBounds(pos)) return false;

    Tile& tile = map.at(pos);

    // Accept both cases:
    // - still a Village (caller didn't flip yet)
    // - already flipped to City by Interaction
    const SettlementTypeEnum st = tile.getSettlementType();
    if (st != SettlementTypeEnum::Village && st != SettlementTypeEnum::City) return false;

    // Resolve / allocate a settlement id.
    SettlementId sid = tile.getSettlementId();
    CityId cid = kNoCity;

    if (sid != kNoSettlement) {
        cid = static_cast<CityId>(sid);
        if (cid < 0) return false;
        if (static_cast<size_t>(cid) >= cities.size()) {
            cities.resize(static_cast<size_t>(cid) + 1);
        }
    } else {
        cid = static_cast<CityId>(cities.size());
        cities.resize(cities.size() + 1);
        sid = static_cast<SettlementId>(cid);
    }

    City& c = cities[static_cast<size_t>(cid)];

    // Initialize / overwrite city data
    c.setCityId(cid);
    c.setOwnerId(static_cast<uint8_t>(owner));
    c.setLevel(1);
    c.setPopulation(0);
    c.setStarsPerRound(1);
    c.setCapital(false);
    if (c.getName().empty()) {
        c.setName(std::string("City ") + std::to_string(static_cast<int>(cid)));
    }

    // Ensure tile points to this city id.
    tile.setSettlement(SettlementTypeEnum::City, sid);

    claimFreeTerritoryRadius1(map, cid, sid, pos);

    // Set ownership visuals/marker on the tile.
    // tile.setTribe(getPlayer(owner).getTribeType());

    // Register the city in the owner's city list (needed for tech price scaling).
    {
        Player& pl = getPlayer(owner);
        const auto& cs = pl.getCities();
        if (std::find(cs.begin(), cs.end(), cid) == cs.end()) {
            pl.addCity(cid);
        }
        if (pl.getCapitalId() == kNoCity) {
            pl.setCapitalId(cid);
        }
    }

    // If a unit is standing on the tile (it is the founder), reassign it to this new city.
    {
        const UnitId uOn = map.unitOn(pos);
        if (uOn != Map::kNoUnit) {
            reassignUnitToCity(cities, uOn, cid);
        }
    }

    return true;
}

