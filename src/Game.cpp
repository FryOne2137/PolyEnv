bool Game::endTurn(PlayerId pid) {
    // Only the current player can end their turn.
    if (!isPlayersTurn(pid)) return false;

    // kończ turę aktualnego gracza
    TurnSystem::endTurn(*this);

    // Recompute city connections after all actions of the turn (roads/bridges/ports/territory changes).
    g_citiesConnectionSystem.update(*this);

    // Pacifist task (Altar of Peace): count consecutive turns with no attacks.
    {
        Player& pl = getPlayer(pid);
        if (pl.getAttackedThisTurn()) {
            pl.resetNoAttackTurns();
        } else {
            pl.incrementNoAttackTurns();
        }
        pl.setAttackedThisTurn(false);
        MonumentSystem::onNoAttackTurnsUpdated(*this, pid, static_cast<int>(pl.getNoAttackTurns()));
    }

    // następny gracz
    currentPlayer = static_cast<PlayerId>((currentPlayer + 1) % players.size());
    if (currentPlayer == 0) {
        turnNumber++;
    }

    // rozpocznij turę nowego gracza
    TurnSystem::startTurn(*this);
    return true;
}

